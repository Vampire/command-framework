package net.kautler.command.usage;

import org.antlr.v4.runtime.BaseErrorListener;
import org.antlr.v4.runtime.RecognitionException;
import org.antlr.v4.runtime.Recognizer;

import static java.lang.String.format;

/**
 * An ANTLR error listener that throws an exception when a syntax error happened.
 */
public class UsageErrorListener extends BaseErrorListener {
    @Override
    public void syntaxError(Recognizer<?, ?> recognizer, Object offendingSymbol,
                            int line, int charPositionInLine,
                            String msg, RecognitionException exception) {
        throw new IllegalArgumentException(
                format("%s [at %d:%d]", msg, line, charPositionInLine));
    }
}
